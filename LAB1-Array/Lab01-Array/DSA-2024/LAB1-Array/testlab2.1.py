##จงเขียนโปรแกรมจัดการคะแนนสอบ:
#เก็บคะแนนสอบ 3 วิชาของนักเรียน 4 คน
#คำนวณคะแนนเฉลี่ยของแต่ละวิชา
#หานักเรียนที่ได้คะแนนรวมสูงสุด
#แสดงจำนวนนักเรียนที่สอบผ่านแต่ละวิชา (คะแนนผ่าน ≥ 50)

# เก็บคะแนนสอบ 3 วิชาของนักเรียน 4 คน
scores = [[85, 95, 60], [67, 60, 100], [80, 78, 65], [90, 72, 53]]

# คำนวณคะแนนเฉลี่ยของแต่ละวิชา
avg_scores = [sum(col) / len(scores) for col in zip(*scores)]
print("คะแนนเฉลี่ยของแต่ละวิชา:", avg_scores)

# หานักเรียนที่ได้คะแนนรวมสูงสุด
max_student = max(enumerate(scores), key=lambda x: sum(x[1]))[0]
print("นักเรียนที่ได้คะแนนรวมสูงสุดคือ คนที่", max_student+1)

# นับจำนวนนักเรียนที่สอบผ่านแต่ละวิชา
pass_count = [sum(score >= 50 for score in col) for col in zip(*scores)]
print("จำนวนนักเรียนที่สอบผ่านแต่ละวิชา:", pass_count)